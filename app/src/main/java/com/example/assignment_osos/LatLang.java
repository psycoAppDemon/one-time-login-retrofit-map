package com.example.assignment_osos;

class LatLng {
    private final String lat;
    private final String lng;

    public LatLng(String lat, String lng) {
        this.lat = lat;
        this.lng = lng;
    }

    public String getLat() {
        return lat;
    }

    public String getLng() {
        return lng;
    }
}
